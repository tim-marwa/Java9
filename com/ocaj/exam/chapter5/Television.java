package com.ocaj.exam.chapter5;

public class Television {

	int channel = 0;
	boolean on = false;
	
	public int getChannel() {
		return channel;
	}
	public void setChannel(int channel) {
		this.channel = channel;
	}
	public boolean isOn() {
		return on;
	}
	public void setOn(boolean on) {
		this.on = on;
	}
		

}
